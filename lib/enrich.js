// POST to the Claude API with retry-with-backoff on TRANSIENT failures (rate
// limits + Anthropic-side overload/5xx + network blips). 503/529 = overloaded,
// common for large Opus requests, and almost always succeeds on a retry. Honors a
// Retry-After header when present; otherwise exponential backoff with jitter.
// Throws (with .status) on non-retryable errors or once retries are exhausted.
const TRANSIENT_STATUS = new Set([429, 500, 502, 503, 529]);
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const backoffMs = (attempt) => Math.min(30000, 1500 * Math.pow(2, attempt)) + Math.floor(Math.random() * 600);
async function claudeFetch(apiKey, body) {
  const MAX = Number(process.env.ENRICH_RETRIES) || 4;
  const TIMEOUT = Number(process.env.ENRICH_TIMEOUT_MS) || 600000;
  let lastErr = null;
  for (let attempt = 0; attempt <= MAX; attempt++) {
    const ctrl = new AbortController();
    const timer = setTimeout(() => ctrl.abort(), TIMEOUT);
    let res;
    try {
      res = await fetch(API_URL, {
        method: 'POST',
        headers: { 'x-api-key': apiKey, 'anthropic-version': '2023-06-01', 'content-type': 'application/json' },
        body: JSON.stringify(body), signal: ctrl.signal,
      });
    } catch (e) {
      clearTimeout(timer);
      if (e.name === 'AbortError') { const err = new Error('the AI build timed out'); err.status = 504; throw err; }
      lastErr = new Error('could not reach the Claude API: ' + e.message); lastErr.status = 503;
      if (attempt < MAX) { await sleep(backoffMs(attempt)); continue; }
      throw lastErr;
    }
    if (res.ok) { const d = await res.json().catch(() => null); clearTimeout(timer); return d; }
    const retryAfter = Number(res.headers.get('retry-after'));
    clearTimeout(timer);
    if (TRANSIENT_STATUS.has(res.status) && attempt < MAX) {
      lastErr = new Error('Claude API HTTP ' + res.status + ' (transient)');
      await sleep(retryAfter > 0 ? retryAfter * 1000 : backoffMs(attempt));
      continue;
    }
    const data = await res.json().catch(() => null);
    const err = new Error((data && data.error && data.error.message) || ('Claude API HTTP ' + res.status)); err.status = res.status; throw err;
  }
  throw lastErr || new Error('Claude API request failed');
}

async function enrich(intake, apiKey, index, onProgress, checkCancel) {
  apiKey = (apiKey && String(apiKey).trim()) || process.env.ANTHROPIC_API_KEY;
  if (!apiKey) { const e = new Error('no Claude API key provided'); e.code = 'no_key'; throw e; }
  emit(onProgress, { t: 'start', label: 'Docs — architecture, requirements & ADRs', agent: true });

  // Agentic: the model reads package.json, the schema, server routes, and config to
  // ground the architecture in reality, then submits the enrichment.
  const submit = { name: 'submit_docs', description: 'Submit the enrichment (architecture layer-data, stack components, request flows, requirement acceptance criteria, ADR bodies) exactly per the field descriptions.', input_schema: ENRICH_SCHEMA };
  const messages = [{ role: 'user', content: intakeBrief(intake) + manifestBlock(index) +
    '\n\n=== YOUR TASK ===\nExplore the codebase (start with package.json, the schema, server routes, and config) so the architecture reflects the REAL stack and structure, then call submit_docs with the enrichment per the schema. The locked decisions in the intake are the source of truth where they differ from the code.' }];
  const r = await runAgent(apiKey, { system: SYSTEM, messages: messages, tools: EXPLORE_TOOLS.concat([submit]), exec: makeExec(index), submitName: 'submit_docs', maxTokens: DOCS_MAX_TOKENS, onProgress: onProgress, label: 'Docs agent', checkCancel: checkCancel });
  emit(onProgress, { t: 'done', label: 'Docs — architecture, requirements & ADRs', tokens: { fresh: r.usage.input, cached: r.usage.cacheRead, output: r.usage.output } });
  if (!r.result) { const err = new Error('the docs pass did not produce output'); err.status = 502; throw err; }
  // The architecture diagram is structured DATA (layers + edges) rendered by a
  // deterministic swim-lane renderer, so no layout-refinement pass is needed.
  return sanitize(r.result);
}

