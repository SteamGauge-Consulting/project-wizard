function usageOf(data, label, max) {
  const u = (data && data.usage) || {};
  const rec = { pass: label, input: u.input_tokens || 0, output: u.output_tokens || 0, max: max, stop: (data && data.stop_reason) || null };
  rec.truncated = rec.stop === 'max_tokens';   // hit the output cap → detail WAS cut
  rec.headroomPct = max ? Math.round((1 - rec.output / max) * 100) : null;
  console.log('[enrich] ' + label + ': in=' + rec.input + ' out=' + rec.output + '/' + max +
    ' (' + rec.headroomPct + '% headroom) stop=' + rec.stop + (rec.truncated ? '  ⚠️ TRUNCATED' : ''));
  return rec;
}

// The project-agnostic 7-phase governance LIBRARY (the development process every
// issue must fit) lives as HTML in docs-kit.js. Strip it to text ONCE and feed the
// FULL library into every plan + cohesion call — grounding issues in the process.
let _govText = null;
function governanceText() {
  if (_govText !== null) return _govText;
  try {
    const src = fs.readFileSync(path.join(__dirname, 'docs-kit.js'), 'utf8');
    const entries = src.match(/"governance\/library\/0[0-7][^"]*":\s*"((?:[^"\\]|\\.)*)"/g) || [];
    _govText = entries.map((s) => {
      let html = '';
      try { html = JSON.parse(s.slice(s.indexOf(':') + 1).trim()); } catch (e) { return ''; }
      return html
        .replace(/<style[\s\S]*?<\/style>/gi, ' ').replace(/<script[\s\S]*?<\/script>/gi, ' ')
        .replace(/<\/(p|h[1-6]|li|tr|section|div)>/gi, '\n').replace(/<li[^>]*>/gi, '\n• ')
        .replace(/<[^>]+>/g, ' ').replace(/&[a-z]+;/gi, ' ')
        .replace(/[ \t]+/g, ' ').replace(/\n[ \t]*\n[ \t]*\n+/g, '\n\n').trim();
    }).filter(Boolean).join('\n\n———\n\n');
  } catch (e) { _govText = ''; }
  return _govText;
}

// The full reference context fed to EVERY plan + cohesion call: the whole intake
